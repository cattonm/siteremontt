// ---- Прив'язка кімнат користувача до зон макета ----
// Кімнати беруться в порядку створення; кожна займає першу вільну зону
// свого типу. Кому зони не вистачило (напр., третя "Кімната") — потрапляє
// в overflow: на плані її не видно, але вона доступна через чипи під планом.
export function assignRoomsToZones(rooms) {
    const used = new Set();
    const roomByZoneId = {};
    const zoneByRoomId = {};

    rooms.forEach((room) => {
        const zone = ZONES.find((z) => z.type === room.type && !used.has(z.id));
        if (zone) {
            used.add(zone.id);
            roomByZoneId[zone.id] = room;
            zoneByRoomId[room.id] = zone.id;
        }
    });

    return {
        roomByZoneId,
        zoneByRoomId,
        overflow: rooms.filter((r) => !zoneByRoomId[r.id]),
    };
}